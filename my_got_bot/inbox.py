"""
ЧТО ДЕЛАЕТ ЭТОТ ФАЙЛ / МОДУЛЬ:
Отвечает за приём входящих сообщений от пользователя. Это первый шаг
в обработке запроса — функция get_user_message() читает текст из консоли,
делает базовую валидацию (проверяет, что сообщение не пустое) и возвращает
чистое сообщение для дальнейшей обработки системой.
"""


def get_user_message():
    """
    Получает сообщение от пользователя из консоли.
    Возвращает текст сообщения или None, если пользователь хочет выйти.
    """
    print("\n" + "=" * 60)
    print("=== INBOX ===")
    print("=" * 60)
    
    user_input = input("Вы: ").strip()
    
    # Проверка на команды выхода
    if user_input.lower() in ["exit", "quit", "выход", "q"]:
        return None
    
    # Проверка на пустое сообщение
    if not user_input:
        print("⚠️  Сообщение не может быть пустым!")
        return ""
    
    return user_input
